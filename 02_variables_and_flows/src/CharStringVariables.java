
public class CharStringVariables {

	public static void main(String args[]) {
		
		//char
		char myChar = 'a';
		System.out.println("My Char: " + myChar);
		
		char myNewChar = 66;
		System.out.println("My New Char: " + myNewChar);
		
		char myNewCastChar = (char) (myNewChar + 1);
		System.out.println("My New Cast Char: " + myNewCastChar);
		
		//String
		String myText = "My new text";
		System.out.println(myText);
		
		//concat
		System.out.println(myText + " in "  + 2021);
	}
	
}
